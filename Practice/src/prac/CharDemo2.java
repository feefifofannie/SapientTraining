//char variables behave like integers. 
/**
 * 
 * @author dredmo
 *
 */
class CharDemo2 {
	/**
	 * 
	 * @param args
	 */
	public static void main(final String[] args) {
		char ch1;

		ch1 = 'X';
		System.out.println("ch1 contains " + ch1);

		ch1++; // increment ch1
		System.out.println("ch1 is now " + ch1);
	}
}